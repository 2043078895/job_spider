	private void getViewUserList() {
		ServiceFacade sf = new ServiceFacade();
		List<ViewUser> list = new ArrayList<ViewUser>();
		list = sf.getViewUserList();
		//table
		Object rowData[][] = new Object[list.size()][3];
		String column[] = { "用户名", "口令", "权限" };
		tb_user = new javax.swing.JTable(rowData, column);
		jScrollPane1.setViewportView(tb_user);

		int i = 0;
		Iterator it = list.iterator();
		while (it.hasNext()) {
			ViewUser vu = (ViewUser) it.next();
			System.out.println(vu);
			rowData[i][0] = vu.getUsername();
			rowData[i][1] = vu.getPassword();
			rowData[i][2] = vu.getPriname();
			i++;
		}
	}

