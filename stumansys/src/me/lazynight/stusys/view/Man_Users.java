	//删除指定用户
	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
		// TODO add your handling code here:
		int row = tb_user.getSelectedRow();
		if (row != -1) {
			System.out.println("当前行：" + row);
			String username = tb_user.getValueAt(row, 0).toString();
			System.out.println("要删除"+username);
			ServiceFacade sf = new ServiceFacade();
			sf.deleteUser(username);
			getViewUserList();
		} else {
			System.out.println("请先选中一行");
		}

	}
